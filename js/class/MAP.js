MAP.prototype.setGrid = function() {
    this.grid.width = Math.floor(this.sizeCanvas.width / 20);
    this.grid.height = Math.floor(this.sizeCanvas.height / 20);

    this.currentBloc = this.grid.height;
};

MAP.prototype.drawGrid = function(context2d, newBloc, margin) {
    if(newBloc)
    {
        this.currentBloc++;
        this.countMargin++;
    }

    console.log(this.currentBloc);

    if(this.currentBloc == this.mapFile.length)
    {
        console.log('endOfMap');

        return;
    }

    for(var y = 0; y < this.currentBloc; y++)
    {
        for(var x = 0; x < this.mapFile[0].length; x++)
        {
            context2d.beginPath();
            context2d.rect(x*20, (((this.grid.height * 20) - (y * 20)) + (margin * this.countMargin)), 20, 20);

            if(this.mapFile[y][x] == 1)
            {
                context2d.fillStyle = 'yellow';
                context2d.fill();
            }
            else
            {
                context2d.fillStyle = 'red';
                context2d.fill();
            }

            context2d.strokeStyle = 'black';
            context2d.stroke();
        }
    }

    for(var x = 0; x < this.currentBloc; x++)
    {
        context2d.beginPath();
        context2d.moveTo(0, x*20 + margin);
        context2d.lineTo(this.sizeCanvas.width, x*20 + margin);
        context2d.stroke();
    }

    for(var y = 0; y < this.grid.height; y++)
    {
        context2d.beginPath();
        context2d.moveTo(y*20, 0);
        context2d.lineTo(y*20, this.sizeCanvas.height);
        context2d.stroke();
    }
};
